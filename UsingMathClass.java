public class UsingMathClass{
	public static void main(String[] args){
		System.out.println("The absolute value is " + Math.abs(-60));
		System.out.println("The greater number is " + Math.max(-60, 25));
		System.out.println("The random value is " + Math.random());
		System.out.println("The lesser number is " + Math.min(-680, 125));
		System.out.println("The squareroot of the number is " + Math.abs(49));
	}
}