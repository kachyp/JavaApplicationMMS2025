

public class TypeCasting{
	public static void main(String[] args){
		
		double balance = 68949;
		System.out.printf("His balance is %f%n",balance);
		
		double number = 89.25;
		int newValue = (int)number;
		System.out.printf("The number is %d%n",newValue);
		
	}
	
}

